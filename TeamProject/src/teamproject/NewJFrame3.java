    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = this.jTextField1.getText();
        //WBS saver = new TestVersion();          //TEST 버전
        WBS saver = new ReleaseVersion();     //RELEASE 버전
        
        saver.WayPrint();
        
        if (id.compareTo(saver.load(id, 0, 0))== 0){ 
            Tracer tracer = new Tracer(sailingData,id);
            tracerList.add(tracer);
            
            //Iterator 패턴을 통한 전체 조회
            Iterator iterator = tracerList.iterator();
            while (iterator.hasNext()) {
                Tracer element = (Tracer) iterator.next();
                java.lang.System.out.println(element);
            }
        }
        this.jTextField1.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = this.jTextField1.getText();
        Iterator iterator = tracerList.iterator();
        while (iterator.hasNext()) {
            Tracer element = (Tracer) iterator.next();
            java.lang.System.out.println(element.getId());
            String str = element.getId();
            if(str.compareTo(id)==0){
                sailingData.removeObserver(element);
            }
        }
        this.jTextField1.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

