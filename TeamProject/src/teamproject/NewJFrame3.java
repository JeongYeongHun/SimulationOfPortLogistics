    private void jScrollBar1AdjustmentValueChanged(java.awt.event.AdjustmentEvent evt) {//GEN-FIRST:event_jScrollBar1AdjustmentValueChanged
        // TODO add your handling code here:
        int num = this.jScrollBar1.getValue();
        int temp2 = 0;
        
        if (prograssBarNum < num){
            prograssBarNum = num;
        }
        this.jScrollBar1.setValue(prograssBarNum);
        this.jProgressBar1.setValue(num);
        
        if(prograssBarNum > 85){
            this.jLabel3.setText("배송완료");
            temp2 = 2;
        }
        else if(prograssBarNum > 5){
            this.jLabel3.setText("배송중");
            temp2 = 1;
        }
        else{
            this.jLabel3.setText("배송전");
            temp2 = 0;
        }
        if(this.temp != temp2){
            if(this.temp == 0){
                Instant i = Instant.now();
                sailingData.setDeliveryStateInfo(this.jList1, "배송중", i.toString());
                jList1.setListData(sailingData.vec); 
                
            }
            else if(this.temp == 1){
                Instant i = Instant.now();
                sailingData.setDeliveryStateInfo(this.jList1, "배송완료", i.toString());
                jList1.setListData(sailingData.vec); 
            }
            this.temp = temp2;
        }
    }//GEN-LAST:event_jScrollBar1AdjustmentValueChanged

